    private void SellAmountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SellAmountButtonActionPerformed
        // TODO add your handling code here:
        int indexSelect = displayTable.getSelectedRow();
        DefaultTableModel dtm = (DefaultTableModel) displayTable.getModel();
        JTextField sellAmount = SellAmountTextField;
        if(Integer.parseInt(sellAmount.getText()) >  (Integer) dtm.getValueAt(indexSelect, 3)){
            JOptionPane.showMessageDialog(null, "Out of range");
        } else {
            int subAmount = (Integer) dtm.getValueAt(indexSelect, 3) - Integer.parseInt(sellAmount.getText());
            list.get(indexSelect).setAmount(subAmount);
            dtm.setValueAt(subAmount, indexSelect, 3);
        }
        JOptionPane.showMessageDialog(null, "Successfully");
        
        SellDialog.setVisible(false);
        displayTable.setEnabled(true);
    }//GEN-LAST:event_SellAmountButtonActionPerformed

