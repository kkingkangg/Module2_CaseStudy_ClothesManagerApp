    private void addButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addButtonMouseClicked
        // TODO add your handling code here:        
            JTextField nameText = nameTextField;
            JComboBox selectSize = sizeSelect;;
            JTextField priceText = priceTextField;
            JTextField amountText = amountTextField;
            DefaultTableModel dtm = (DefaultTableModel) displayTable.getModel();
            NumberFormat nf = NumberFormat.getInstance(new Locale("en", "US"));
            Regex regex = new Regex();
            
            if((!regex.string(nameText.getText())) || (!regex.number1(priceText.getText())) || (!regex.number1(amountText.getText()))){
                JOptionPane.showMessageDialog(null, "Please re-enter your information");
            } else{
            for (Clothes obj : list) {
            if ((obj.getName().equals(nameText.getText())) && obj.getSize().equals((String)selectSize.getSelectedItem())) {
                if (obj.getPrice() != Integer.parseInt(priceText.getText())) {
                    JOptionPane.showMessageDialog(null, "The product already in the system, please re-enter the price");
                    return;
                } else {
                    int sumAmount = obj.getAmount() + Integer.parseInt(amountText.getText());
                    JOptionPane.showMessageDialog(null, "The product already in the system, increasing amount");
                    obj.setAmount(sumAmount);                                    
//                       truyền thay đổi vào bảng table
                    for (int i = 0; i < dtm.getRowCount(); i++) {
                        if ((dtm.getValueAt(i, 1).equals(nameText.getText())) && (dtm.getValueAt(i, 2).equals((String)selectSize.getSelectedItem()))) {
                            list.get(i).setAmount(sumAmount);
                            dtm.setValueAt(sumAmount, i, 3);
                        }
                    }
                    return;
                }                
            }
        }
            
        Clothes clothes = new Clothes(nameText.getText(), (String)selectSize.getSelectedItem(), Integer.parseInt(amountText.getText()), Integer.parseInt(priceText.getText()));
        list.add(clothes);
        dtm.addRow(new Object[]{clothes.getId(), nameText.getText(), (String)selectSize.getSelectedItem(), Integer.parseInt(amountText.getText()), nf.format(Integer.parseInt(priceText.getText()))});           
            }
    }//GEN-LAST:event_addButtonMouseClicked

